class Solution {
  public:
    long long func(Node* root, long long& temp)
    {
        if (root==nullptr)
            return 1;
        long long left=func(root->left, temp);
        long long right=func(root->right, temp);
        long long subtreeMax=max({left, right, 1LL})*(root->data);
        temp=max({temp*(root->data), 1LL});
        return subtreeMax;
    }
    long long findMaxScore(Node* root)
    {
        long long temp=1;
        return func(root, temp);
    }
};

