class Solution {
  public:
    int getSingle(vector<int> &arr)
    {
        unordered_map<int, int>mp;
        for (int i=0;i<arr.size();i++)
            mp[arr[i]]++;
        for (auto i=mp.begin();i!=mp.end();i++)
        {
            if (i->second!=3)   
                return i->first;
        }
        return -1;
    }
};


