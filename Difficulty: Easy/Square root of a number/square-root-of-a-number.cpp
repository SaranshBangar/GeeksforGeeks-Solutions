// Function to find square root
// x: element to find square root
class Solution {
  public:
    long long int floorSqrt(long long int n)
    {
        long long int i=1, check=1;
        while (check<=n)
        {
              i++;
              check=i*i;
        }
        return i-1;
    }
};

//{ Driver Code Starts.

int main() {
    int t;
    cin >> t;
    while (t--) {
        long long n;
        cin >> n;
        Solution obj;
        cout << obj.floorSqrt(n) << endl;
    }
    return 0;
}

